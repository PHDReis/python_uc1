a = 10
b = 3

soma=a+b
subtracao=a-b
multiplicacao=a*b
divisao=a/b
div_inteiro=a//b
resto=a%b
exponenciacao=a**b

print(f"O resultado da soma é {soma}")
print(f"O resultado da subtração é {subtracao}")
print(f"O resultado da multiplicação é {multiplicacao}")
print(f"O resultado da divisão é {divisao}")
print(f"O resultado da parte inteira da divisão é {div_inteiro}")
print(f"O resultado do resto da divisão é {resto}")
print(f"O resultado da exponenciação é {exponenciacao}")

## a "#" serve para colocar os comentários no código ou até mesmo desativar um pedaço do código para teste
## No inicio da string, logo depois do print, o "F" serve para uma formatação especial onde o Python vai mesclar a string com a variavel, fazendo com que a linha funcione. Caso não seja colocado o F, é nenessário reescrever a linha para que ela funcione corretamente, do contrário será interpretado tudo como texto
