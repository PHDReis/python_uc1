x=10
print(f"O valor inicial de x é {x}")
## adicao
x+=5
print(f"O valor da adição de x é {x}")
## subtracao
x-=3
print(f"O valor da subtração de x é {x}")
## multiplicacao
x*=2
print(f"O valor da multiplicação de x é {x}")
## divisao
x/=4
print(f"O valor da divisão de x é {x}")
## div_int
x//=3
print(f"O valor da divisão inteira de x é {x}")
## resto
x%=2
print(f"O valor da módulo de x é {x}")
## expo
x**=3
print(f"O valor da exponenciação de x é {x}")
